class BertModel(tf.keras.Model):
    def __init__(self, preprocessing, encoder, title, content, fine_tune=True):
        # super(BertModel, self).__init__(**kwargs)
        super().__init__()
        self.input_layer = tf.keras.layers.Input(shape=(), dtype=tf.string, name='input_text')
        self.preprocessing_layer = hub.KerasLayer(preprocessing, name='preprocessing')
        self.encoder_layer = hub.KerasLayer(encoder, trainable=not fine_tune, name='BERT_encoder')
        self.title_classifier = tf.keras.layers.Dense(title, activation='relu', name='title_classifier')
        self.content_classifier = tf.keras.layers.Dense(content, activation='relu', name='content_classifier')
        self.classifier = tf.keras.layers.Dense(1, activation='sigmoid', name='combined_classifier')

    def call(self, inputs, **kwargs):
        title, content = inputs
        title = self.preprocessing_layer(title)
        content = self.preprocessing_layer(content)
        # title = self.encoder_layer(title)
        # content = self.encoder_layer(content)
        title = self.encoder_layer(title)['pooled_output']
        content = self.encoder_layer(content)['pooled_output']
        title = self.title_classifier(title)
        content = self.content_classifier(content)
        combined = tf.keras.layers.concatenate([title, content])
        return self.classifier(combined)


def load_handle(model_name=bert_model_name):
    """Loads a model from TFHub."""
    tfhub_handle_encoder = map_name_to_handle[model_name]
    tfhub_handle_preprocess = map_model_to_preprocess[model_name]
    print(f'BERT model selected           : {tfhub_handle_encoder}')
    print(f'Preprocess model auto-selected: {tfhub_handle_preprocess}')
    return tfhub_handle_encoder, tfhub_handle_preprocess

